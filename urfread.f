c$$$      write(*,*)'nray(krf),nharms(krfn(krf)),freqcy(krf),nharm(krf)',
c$$$     1           nray(krf),nharms(krfn(krf)),freqcy(krf),nharm(krf)
c$$$      write(*,*)'urfread:nrayelt(*,krf)',(nrayelt(i,krf),i=1,nray(krf))
c$$$      write(*,*)'urfread:ws(1,*,krf)',(ws(1,i,krf),i=1,nray(krf))
c$$$      write(*,*)'urfread:spsi(1,*,krf)',(spsi(1,i,krf),i=1,nray(krf))
c$$$      write(*,*)'urfread:wr(1,*,krf)',(wr(1,i,krf),i=1,nray(krf))
c$$$      write(*,*)'urfread:wz(1,*,krf)',(wz(1,i,krf),i=1,nray(krf))
c$$$      write(*,*)'urfread:wphi(1,*,krf)',(wphi(1,i,krf),i=1,nray(krf))
c$$$      write(*,*)'urfread:wnpar(1,*,krf)',(wnpar(1,i,krf),i=1,nray(krf))
c$$$      write(*,*)'urfread:wnper(1,*,krf)',(wnper(1,i,krf),i=1,nray(krf))
c$$$      write(*,*)'urfread:delpwr(1,*,krf)',
c$$$     1     (delpwr(1,i,krf),i=1,nray(krf))
c$$$      write(*,*)'urfread:sdpwr(1,*,krf)',(sdpwr(1,i,krf),i=1,nray(krf))
c$$$      write(*,*)'urfread:wdnpar(1,*,krf)',
c$$$     1     (wdnpar(1,i,krf),i=1,nray(krf))
c$$$      write(*,*)'urfread:cwexde(1,*,krf)',
c$$$     1     (cwexde(1,i,krf),i=1,nray(krf))
c$$$      write(*,*)'urfread:cweyde(1,*,krf)',
c$$$     1     (cweyde(1,i,krf),i=1,nray(krf))
c$$$      write(*,*)'urfread:cwezde(1,*,krf)',
c$$$     1     (cwezde(1,i,krf),i=1,nray(krf))
c$$$      write(*,*)'urfread:fluxn(1,*,krf)',(fluxn(1,i,krf),i=1,nray(krf))
c$$$      write(*,*)'urfread:sbtot(1,*,krf)',(sbtot(1,i,krf),i=1,nray(krf))
c$$$      write(*,*)'urfread:sene(1,*,krf)',(sene(1,i,krf),i=1,nray(krf))
c$$$      write(*,*)'urfread:salphac(1,*,krf)',
c$$$     1     (salphac(1,i,krf),i=1,nray(krf))
c$$$      write(*,*)'urfread:salphal(1,*,krf)',
c$$$     1     (salphal(1,i,krf),i=1,nray(krf))
c$$$
c$$$      write(*,*)'urfread:ws(2,*,krf)',(ws(2,i,krf),i=1,nray(krf))
c$$$      write(*,*)'urfread:spsi(2,*,krf)',(spsi(2,i,krf),i=1,nray(krf))
c$$$      write(*,*)'urfread:wr(2,*,krf)',(wr(2,i,krf),i=1,nray(krf))
