c$$$      write(*,*)'urfread:wphi(2,*,krf)',(wphi(2,i,krf),i=1,nray(krf))
c$$$      write(*,*)'urfread:wnpar(2,*,krf)',(wnpar(2,i,krf),i=1,nray(krf))
c$$$      write(*,*)'urfread:wnper(2,*,krf)',(wnper(2,i,krf),i=1,nray(krf))
c$$$      write(*,*)'urfread:delpwr(2,*,krf)',
c$$$     1     (delpwr(2,i,krf),i=1,nray(krf))
c$$$      write(*,*)'urfread:sdpwr(2,*,krf)',(sdpwr(2,i,krf),i=1,nray(krf))
c$$$      write(*,*)'urfread:wdnpar(2,*,krf)',
c$$$     1     (wdnpar(2,i,krf),i=1,nray(krf))
c$$$      write(*,*)'urfread:cwexde(2,*,krf)',
c$$$     1     (cwexde(2,i,krf),i=1,nray(krf))
c$$$      write(*,*)'urfread:cweyde(2,*,krf)',
c$$$     1     (cweyde(2,i,krf),i=1,nray(krf))
c$$$      write(*,*)'urfread:cwezde(2,*,krf)',
c$$$     1     (cwezde(2,i,krf),i=1,nray(krf))
c$$$      write(*,*)'urfread:fluxn(2,*,krf)',(fluxn(2,i,krf),i=1,nray(krf))
c$$$      write(*,*)'urfread:sbtot(2,*,krf)',(sbtot(2,i,krf),i=1,nray(krf))
c$$$      write(*,*)'urfread:sene(2,*,krf)',(sene(2,i,krf),i=1,nray(krf))
c$$$      write(*,*)'urfread:salphac(2,*,krf)',
c$$$     1     (salphac(2,i,krf),i=1,nray(krf))
c$$$      write(*,*)'urfread:salphal(2,*,krf)',
c$$$     1     (salphal(2,i,krf),i=1,nray(krf))
c$$$      enddo
c$$$      write(*,*)'****************************************************'
 
      return
      end
